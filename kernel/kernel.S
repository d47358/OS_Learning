%define ERROR_CODE nop
%define ZERO push 0

extern idt_table
extern syscall_table

section .data
global intr_entry_table
intr_entry_table:
%macro VECTOR 2
section .text
intr%1entry:
    %2
    push ds
    push es
    push fs
    push gs
    pushad ;EAX,ECX,EDX,EBX,ESP(初始值)，EBP,ESI,EDI

    mov al,0x20
    out 0xa0,al
    out 0x20,al ;向主片从片发送EOI,结束中断
    push %1 ;压入中断向量号(中断处理程序可能需要参数)
    call [idt_table+%1*4] ;调用中断处理函数
    jmp intr_exit

section .data
    dd intr%1entry
%endmacro

section .text
global intr_exit
intr_exit: ;恢复上下文
    add esp,4 ;跳过中断向量号
    popad
    pop gs
    pop fs
    pop es
    pop ds
    add esp,4 ;跳过errorcode
    iretd

section .text
global syscall_handler
syscall_handler:
    push 0
    push ds
    push es
    push fs
    push gs
    pushad
    push 0x80
    push edx
    push ecx
    push ebx
    call [syscall_table + 4 * eax]
    add esp, 12
    mov [esp + 8 * 4], eax
    jmp intr_exit

